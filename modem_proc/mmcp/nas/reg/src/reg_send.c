      msg = (mmr_plmn_search_req_s_type *)modem_mem_alloc(sizeof(mmr_plmn_search_req_s_type), MODEM_MEM_CLIENT_NAS);
#else
      msg = (mmr_plmn_search_req_s_type *)gs_alloc(sizeof(mmr_plmn_search_req_s_type));
